#pede para inserir os graus desejados
temperatura = float (input("Digite uma temperatura:"))
#cria a variavel para celcius
celcius = float (temperatura *1.8 +32)
#cria a variavel para farenheit
Fahrenheit = float (temperatura -32 * 1.8 ) 
#exibe as temperaturas convertidas nas suas respectivas formulas
print (f" celcius:{celcius}" )
print (f" Fahrenheit: {Fahrenheit}")
